/**
 * country object를 만드는 함수
 */
function Country(countryCd, countryNm) {
	return {
		cntryCd: countryCd,
		cntryNm: countryNm,
		label: countryNm 
	};
}

function getCountryList(portList) {
	var countryList = [];
	var cntryCd = '';
	
	for (var i = 0 ; i < portList.length ; i++) {
		if (cntryCd != portList[i].cntryCd) {
			cntryCd = portList[i].cntryCd;
			countryList.push(Country(portList[i].cntryCd, portList[i].cntryNm));
		}
	}
	
	return countryList;
}

function getLabel(portCd) {
	var portList = getPortList();
	var label = '';
	
	for (var i = 0 ; i < portList.length ; i++) {
		if (portCd == portList[i].portCd) {
			label = portList[i].portNm + " / " + portList[i].cntryNm; 
		}
	}
	
	return label;
}

/**
 * port object를 만드는 함수
 */
function Port(countryCd, countryNm, portCd, portNm) {
	return {
		cntryCd: countryCd,
		cntryNm: countryNm,
		portCd: portCd,
		portNm: portNm,
		label: portNm + "(" + portCd + ") / " + countryNm 
	};
}

